# =====================================================================
# プログラム: 配列の最小値・最大値・XORチェックサム計算
# =====================================================================
# gen_isa.yaml の全16命令を網羅的に検証する
#
# アルゴリズム:
#   1. 入力配列 {5, -3, 8, 2, -1} をメモリに格納  (loadi / store)
#   2. 各要素を走査し最小値・最大値を更新          (load / sub / blt / add)
#   3. 各要素でビット演算サブルーチンを呼ぶ        (jal / jalr)
#      - AND・OR・XOR・NOT・SLA・SRA・ADDI を含む
#   4. XOR チェックサムを累積                      (xor)
#   5. ループ終了判定                              (addi / bz)
#   6. ループ継続判定 (カウンタ > 0 チェック)      (sub / blt)
#   7. 結果をメモリへ保存                          (store)
#   8. 無限ループで停止                            (jal)
#
# メモリマップ:
#   Mem[0..4] : 入力配列
#   Mem[5]    : 最小値
#   Mem[6]    : 最大値
#   Mem[7]    : XOR チェックサム
#
# レジスタ規約:
#   r0  : ゼロレジスタ (常に 0、書き込み無効)
#   r1  : ループカウンタ
#   r2  : 配列インデックス (メモリアドレス)
#   r3  : 現在の配列要素
#   r4  : 最小値 (min)
#   r5  : 最大値 (max)
#   r6  : XOR チェックサム
#   r7  : 一時レジスタ
#   r8-r11 : bitops サブルーチン内一時レジスタ
#   r15 : リンクレジスタ (jalr でのリターン先)
# =====================================================================

main:
    # ---- 配列の初期化: loadi + store ----
    loadi r3,  5        # r3 = 5
    store 0, r0, r3     # Mem[0] = 5

    loadi r3, -3        # r3 = -3  (符号付き即値)
    store 1, r0, r3     # Mem[1] = -3

    loadi r3,  8        # r3 = 8
    store 2, r0, r3     # Mem[2] = 8

    loadi r3,  2        # r3 = 2
    store 3, r0, r3     # Mem[3] = 2

    loadi r3, -1        # r3 = -1
    store 4, r0, r3     # Mem[4] = -1

    # ---- ループ変数・累積器の初期化 ----
    loadi r1, 5         # カウンタ = 5 (要素数)
    loadi r2, 0         # インデックス = 0
    load  r4, r0, 0     # min = Mem[0] = 5 (初期値)
    load  r5, r0, 0     # max = Mem[0] = 5 (初期値)
    loadi r6, 0         # checksum = 0

# ---- メインループ ----
loop:
    load  r3, r2, 0     # r3 = Mem[r2]  ← load 命令

    # 最小値更新チェック: r3 < r4 なら set_min へ
    # sub 後 N^V=1 (符号付き less than) のとき blt 分岐
    sub   r7, r3, r4    # r7 = r3 - min; フラグ更新
    blt   set_min       # if r3 < min → set_min  ← blt 命令
    # 分岐、ジャンプのあとは必ずNOP
    addi r0, r0, 0     # NOP (分岐後の命令は必ず実行される)

    # 最大値更新チェック: max < r3 なら set_max へ
after_min:
    sub   r7, r5, r3    # r7 = max - r3; フラグ更新
    blt   set_max       # if max < r3 → set_max  ← blt 命令
    addi r0, r0, 0     # NOP (分岐後の命令は必ず実行される)

    # ---- ビット演算サブルーチン呼び出し: jal ----
after_max:
    jal   r15, bitops   # r15 = PC+2; PC = bitops  ← jal 命令
    addi  r0, r0, 0     # NOP (jal の次の命令は必ず実行される)

    # ---- XOR チェックサム累積 ----
    xor   r6, r6, r3    # checksum ^= r3  ← xor 命令

    # ---- ループ制御 ----
    addi  r2, r2, 1     # インデックス++   ← addi 命令 (メモリはワード単位)
    addi  r1, r1, -1    # カウンタ--        (Z フラグ更新)
    bz    done          # カウンタ == 0 なら終了  ← bz 命令
    addi  r0, r0, 0     # NOP (bz の次の命令は必ず実行される)

    # カウンタがまだ正: sub で r1>0 を確認し blt でループへ
    sub   r7, r0, r1    # r7 = 0 - r1; r1>0 なら r7<0 → N^V=1
    blt   loop          # if r1 > 0 → ループ継続  ← blt 命令
    addi r0, r0, 0     # NOP (blt の次の命令は必ず実行される)

# ---- 結果をメモリへ保存して停止 ----
done:
    store 5, r0, r4     # Mem[5] = min
    store 6, r0, r5     # Mem[6] = max
    store 7, r0, r6     # Mem[7] = checksum

end:
    jal   r0, end       # 無限ループ (r0 は常に 0 なのでリターン不要)  ← jal 命令
    addi r0, r0, 0     # NOP (jal の次の命令は必ず実行される)

# =====================================================================
# set_min: 最小値を更新して after_max へ戻る
# =====================================================================
set_min:
    add   r4, r3, r0    # min = r3 + 0  ← add 命令 (add で代入を検証)
    jal   r0, after_max # after_max へジャンプ (r0 破棄 = 無条件ジャンプ)
    addi r0, r0, 0     # NOP (jal の次の命令は必ず実行される)

# =====================================================================
# set_max: 最大値を更新して after_max へ戻る
# =====================================================================
set_max:
    add   r5, r3, r0    # max = r3 + 0  ← add 命令
    jal   r0, after_max # after_max へジャンプ
    addi r0, r0, 0     # NOP (jal の次の命令は必ず実行される)

# =====================================================================
# bitops: ビット演算・シフト演算をまとめて検証するサブルーチン
# 引数  : r3 (対象要素)
# 破壊  : r7-r11 (メインループ側では未使用)
# リターン: jalr r0, r15, 0
# =====================================================================
bitops:
    loadi r7, 15        # マスク = 0x0F  ← loadi 命令 (サブルーチン内でも使用)

    and   r8, r3, r7    # r8 = r3 & 0x0F  ← and 命令 (下位4ビット抽出)
    or    r9, r3, r7    # r9 = r3 | 0x0F  ← or  命令 (下位4ビットを全1)
    xor   r10, r8, r9   # r10 = r8 ^ r9   ← xor 命令 (差分ビット検出)
    not   r11, r3       # r11 = ~r3        ← not 命令 (全ビット反転)

    loadi r7, 2         # シフト量 = 2
    sla   r8, r8, r7    # r8 = (r3 & 0x0F) << 2  ← sla 命令 (算術左シフト)
    sra   r8, r8, r7    # r8 >>= 2 (符号拡張)    ← sra 命令 (算術右シフト)

    addi  r8, r8, 1     # r8 += 1  ← addi 命令 (シフト後オフセット加算)

    jalr  r0, r15, 0    # PC = r15 + 0 → メインループへリターン  ← jalr 命令
    addi r0, r0, 0     # NOP (jalr の次の命令は必ず実行される)